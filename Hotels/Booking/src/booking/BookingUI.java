    public void setDetails_B(String Destination,String hotel,String hotel_add,String rm_type,int room, int adult,int child,String c_in, String c_out,int price)
    {
        city = Destination;
        hotel_name = hotel;
        hotel_address = hotel_add;
        room_typ = rm_type;
        rooms = room;
        adults = adult;
        childs = child;
        Check_in_dt = c_in;
        Check_out_dt = c_out;
        
        //converting date from string into original date form.
        try
        {
            SimpleDateFormat sdf = new SimpleDateFormat("EEEE, dd MMM, yyyy");
            check_in_dt = sdf.parse(c_in);
            check_out_dt = sdf.parse(c_out);
            DateFormat df = new SimpleDateFormat("dd-MM-yyyy");
            check_in_db = df.format(check_in_dt);
            check_out_db = df.format(check_out_dt);
            //JOptionPane.showMessageDialog(null, check_in_db + " " + check_out_db);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        //calculating total.
        int days = check_out_dt.getDay() - check_in_dt.getDay() + 1;
        total = price*room*(days-1);
        showDetails(city,hotel_name,hotel_address,room_typ,rooms,adults,childs,Check_in_dt,Check_out_dt,total,days);
        //JOptionPane.showMessageDialog(null,city + " " + room_typ + " " + rooms + " " + adults + " " + childs + " " + Check_in_dt + " " + Check_out_dt + " " + total);
    }   
    
